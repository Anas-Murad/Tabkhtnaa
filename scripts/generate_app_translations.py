def php_str(value: str) -> str:
    return "'" + value.replace("\\", "\\\\").replace("'", "\\'") + "'"


def main() -> None:
    with open(KEYS_JSON, encoding="utf-8") as fh:
        ar_map: dict[str, str] = json.load(fh)
    ar_map.update(AR_OVERRIDES)

    all_keys = sorted(set(ar_map) | set(EN) | set(FR))
    missing_en = [k for k in all_keys if k not in EN]
    missing_fr = [k for k in all_keys if k not in FR]
    if missing_en or missing_fr:
        raise SystemExit(f"Missing translations: en={missing_en} fr={missing_fr}")

    lines = [
        "<?php",
        "",
        "/**",
        " * App UI translations — generated by scripts/generate_app_translations.py",
        " * Do not edit manually; regenerate after adding keys in the Flutter app.",
        " */",
        "",
        "return [",
    ]

    for key in all_keys:
        ar = ar_map.get(key, EN[key])
        en = EN[key]
        fr = FR[key]
        lines.append(
            f"    ['key' => {php_str(key)}, 'model' => 'app', "
            f"'ar' => {php_str(ar)}, 'en' => {php_str(en)}, 'fr' => {php_str(fr)}],"
        )

    lines.append("];")
    lines.append("")

    os.makedirs(os.path.dirname(OUT_PHP), exist_ok=True)
    with open(OUT_PHP, "w", encoding="utf-8", newline="\n") as fh:
        fh.write("\n".join(lines))

    print(f"Generated {len(all_keys)} translations -> {OUT_PHP}")


if __name__ == "__main__":
    main()
